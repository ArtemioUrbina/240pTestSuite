/* Print a string at x, y using sprites by VDP1*/
void DrawString(char *str, unsigned int x, unsigned int y, unsigned int palette)
{
	int orig_x = x;
	int orig_pal = palette;
	unsigned int i;
	int highlight = 0;

	for(i = 0; i < strlen(str); i++)
	{
		if(str[i] == '\n')
		{
			x = orig_x;
			y += _fh;
			i++;
		}

		if(str[i] == '#')
		{
			highlight = !highlight;
			i++;
			
			if(highlight)
			{
				switch(str[i])
				{
					case 'R':
						palette = FONT_RED;
						break;
					case 'G':
						palette = FONT_GREEN;
						break;
					case 'C':
						palette = FONT_CYAN;
						break;
					case 'Y':
						palette = FONT_YELLOW;
						break;
					case 'W':
						palette = FONT_WHITE;
						break;
				}
			}
			else
				palette = orig_pal;
			i++;
		}

		DrawChar(x, y, str[i], palette, true);
		x += _fw;
	}
}

